package Day3_04082024;

public class T2_IfElseConditions {
    public static void main(String[]args){

        int a=10;
        int b=20;

        //example with if statement
        if(a<b){
            System.out.println("a is less than b");
        }//end of if

        //example with if else statement
        if(a>b){
            System.out.println("Automation is awesome");
        }else{
            System.out.println("Tomorrow is off");
        }//end of if else

    }//end of main
}//end of class
